def find_answer(question: str) -> str:
    if is_yandex_configured():
        try:
            return generate_yandex_answer(question)
        except ValueError as exc:
            # Cat validation error - return directly without trying FAQ
            return str(exc)
        except Exception as exc:
            # API or network error - try FAQ fallback
            logger.debug(f'YandexGPT error: {exc}')
            pass
    
    q = question.lower()
    for item in FAQ:
        if any(kw in q for kw in item['keywords']):
            return item['answer']
    return (
        'Я пока не нашёл точного ответа в базе знаний. '
        'Опишите симптомы подробнее или задайте вопрос на форуме экспертам. '
        'При острых состояниях обращайтесь к ветеринару немедленно.'
    )
